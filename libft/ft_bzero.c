#include "libft.h"
#include <strings.h>

void	ft_bzero(void *ptr, size_t count)
{
	size_t	i;

	if (!ptr)
		return ;
	i = 0;
	while (i < count)
	{
		*(char *)(ptr + i) = 0;
		i++;
	}
}
